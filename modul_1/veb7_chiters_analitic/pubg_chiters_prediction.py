import json
# import pprint

# Id – уникальный номер игрока
# matchDuration – продолжительность матча в секундах
# heals – количество использованных лечебных предметов
# revives – количество раз, когда игрок оживлял товарищей по команде
# swimDistance – общее расстояние, пройденное в плавании, измеряется в метрах
# walkDistance – общее расстояние, пройденное пешком, измеряется в метрах
# rideDistance – общее расстояние, пройденное на транспорте, измеренное в метрах
# teamKills – количество выбывших товарищей в команде
# headshotKills – количество вражеских игроков, которые получили фраги за хэдшоты и выбыли
# killStreaks – максимальное количество вражеских игроков, которые выбыли за короткий промежуток времени
# kills – количество выбывших игроков противника
# longestKill – наибольшее расстояние между игроком и выбывшим игроком на момент выбывая из игры
# roadKills – количество фрагов, совершенных с помощью транспорта
# weaponsAcquired – количество подобранного оружия

# Максимальная скорость на транспорте – 152 км/ч
# Максимальная скорость пешком – 6.3 км/ч

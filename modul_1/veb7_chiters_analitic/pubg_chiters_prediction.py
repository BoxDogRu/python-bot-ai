# Пример 1. Поиск читеров по киллам и хедшотам.
# Гипотеза 1. Больше 8 фрагов (kills), при этом все попадания в голову (headshots)

cheaters = []   # участников с подозрением в читерстве складываем сюда
for player in player_stats:
    if player['kills'] > 8 and player['headshotKills'] == player['kills']:
        cheaters.append(player['Id'])
print('1. Больше 8 фрагов, при этом все попадания в голову (headshots):', len(cheaters))

